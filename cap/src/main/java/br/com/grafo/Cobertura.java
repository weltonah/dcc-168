package br.com.grafo;

public class Cobertura {
	private int nr;
	private int mi;
	private int ci;
	public int getNr() {
		return nr;
	}
	public void setNr(int nr) {
		this.nr = nr;
	}
	public int getMi() {
		return mi;
	}
	public void setMi(int mi) {
		this.mi = mi;
	}
	public int getCi() {
		return ci;
	}
	public void setCi(int ci) {
		this.ci = ci;
	}
}
